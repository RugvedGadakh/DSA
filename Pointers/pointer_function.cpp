#include<iostream>
using namespace std;
// -------------------------------------------------------------------------------------------->
void print(int *p){

    cout<<*p<<endl;
}
// -------------------------------------------------------------------------------------------->
void UpdateAdd(int *p){
    p = p+1;
    cout<<"inside address : "<<p<<endl;
}
// -------------------------------------------------------------------------------------------->
void UpdateValue(int *p){
    *p = *p + 1;
    cout<<"inside value : "<<*p<<endl;
}
// -------------------------------------------------------------------------------------------->

int getSum(int *arr , int n){
    cout<<endl<<"size : "<<sizeof(arr)<<endl;  //8
    int sum =0;
    for(int i =0;i<n;i++){
        sum+= arr[i];
    }
    return sum;
}
// -------------------------------------------------------------------------------------------->
// -------------------------------------------------------------------------------------------->
// -------------------------------------------------------------------------------------------->

int main(){

    int value =5;

    int *p = &value;
    print(p);
// -------------------------------------------------------------------------------------------->
    cout<<"before address : "<<p<<endl;
    UpdateAdd(p);
    cout<<"after address : "<<p<<endl;
// -------------------------------------------------------------------------------------------->
    cout<<"before value : "<<*p<<endl;
    UpdateValue(p);
    cout<<"after value : "<<*p<<endl;
// -------------------------------------------------------------------------------------------->

    int arr[6]={1,2,3,4,5,8};
    cout<<"sum is : "<<getSum(arr,6)<<endl;
    cout<<"sum is : "<<getSum(arr+3,3)<<endl;
}